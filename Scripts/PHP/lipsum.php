    static public function getTitle()
    {
        $title = (Helper::random(2,1) == 1)? self::artists(): self::superHeores();
        $title .= " ";

        $tmpVal = self::actions();
        if($tmpVal[1] === 1)
        {
            $action = self::adverbs() . " " . $tmpVal[0] . " ";
            $action .= (Helper::random(2,1) == 1)? self::artists(): self::superHeores();
        } else {
            $action =  $tmpVal[0] . " " . self::adverbs();
        }

        $title .= $action;

        if(Helper::random(2,1) == 1)
        {
            $title .= self::countries();
        }

        return $title;
    }

    static public function getParagraph()
    {
        $paragraph = "";
        $count= Helper::random(8,3);
        for ($i=0; $i <=  $count; $i++)
        {
            $paragraph .= trim(self::getTitle());
            if($i < $count -1){   $paragraph .=  self::conectors();}
        }
        return $paragraph . ".";
    }

    static public function getBody()
    {
        $count= Helper::random(10,3);
        $body = "";
        for ($i=0; $i <=  $count; $i++)
        {
            $body .= self::getParagraph() . "\n\n";
        }
        return $body;
