class Helper {

    static private function __makeSeed()
    {
        list($usec, $sec) = explode(' ', microtime());
        srand($sec + $usec * 1000000);
    }

    public function random($max, $min = 0){
        self::__makeSeed();

        if(is_array($max))
        {
            return $max[rand($min, count($max) -1)];
        }
        if(is_int($max))
        {
            return rand($min,$max);
        }
        throw new InvalidArgumentException("[Helper::random] The \$max argument must be an array or an integer.");
    }
}

